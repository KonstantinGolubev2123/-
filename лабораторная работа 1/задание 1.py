numbers = [2, -93, -2, 8, None, -44, -1, -85, -14, 90, -22, -90, -100, -8, 38, -92, -45, 67, 53, 25]

# Исходный список с пропущенным элементом
numbers = [2, -93, -2, 8, None, -44, -1, -85, -14, 90, -22, -90, -100, -8, 38, -92, -45, 67, 53, 25]

# Шаг 1: Найдем индекс пропущенного элемента
missing_index = numbers.index(None)

# Шаг 2: Вычислим сумму всех чисел, исключая пропущенный элемент
sum_numbers = sum(x for x in numbers if x is not None)

# Шаг 3: Подсчитаем количество элементов, включая пропуск
count_numbers = len(numbers)

# Шаг 4: Рассчитаем среднее арифметическое
average = sum_numbers / count_numbers

# Шаг 5: Заменим пропущенный элемент средним арифметическим
numbers[missing_index] = average

# Выводим измененный список
print("Измененный список:", numbers)
